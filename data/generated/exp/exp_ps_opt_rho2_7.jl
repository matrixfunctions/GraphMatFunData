    coeff4=-5.249610150102727e-19
    coeff5=4.267589684021346e-27
    memslots[3] .= coeff2.*memslots[1] .+ coeff3.*memslots[4] .+ coeff4.*memslots[5] .+ coeff5.*memslots[6]
